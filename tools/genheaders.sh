#!/bin/bash

for d in `find include/02_ood_ecs -type d ! -name 02_ood_ecs` ; do
	b=`basename $d`
	u=`echo -n $b | tr /a-z/ /A-Z/`
	echo -e "// THIS FILE WAS AUTOMATICALLY GENERATED BY genheaders.sh" > $d.hpp
	echo -e "// DO NOT EDIT MANUALLY\n" >> $d.hpp
	echo -e "#ifndef ${u}_HPP\n#define ${u}_HPP\n" >> $d.hpp
	for f in `find $d -name '*.hpp'`; do
		echo -e "#include \"$b/`basename $f`\"" >> $d.hpp;
	done
	echo -ne "\n#endif // ${u}_HPP" >> $d.hpp;
done

echo -e "// THIS FILE WAS AUTOMATICALLY GENERATED BY genheaders.sh" > include/02_ood_ecs.hpp
echo -e "// DO NOT EDIT MANUALLY\n" >> include/02_ood_ecs.hpp
echo -e "#ifndef OOD_ECS_HPP\n#define OOD_ECS_HPP\n" > include/02_ood_ecs.hpp
for f in `cd include && find 02_ood_ecs -maxdepth 1 -name '*.hpp'` ; do
	echo -e "#include \"$f\"" >> include/02_ood_ecs.hpp;
done
echo -e "\n#endif // OOD_ECS_HPP" >> include/02_ood_ecs.hpp
